POLICY_RULES = {

    # =====================================
    # EMAIL
    # =====================================

    "EMAIL": "MASK",

    # =====================================
    # PHONE
    # =====================================

    "PHONE": "TOKENIZE",

    # =====================================
    # PAN
    # =====================================

    "PAN": "MASK",

    # =====================================
    # AADHAAR
    # =====================================

    "AADHAAR": "REDACT",

    # =====================================
    # IP ADDRESS
    # =====================================

    "IP_ADDRESS": "MASK",

    # =====================================
    # ACCOUNT
    # =====================================

    "ACCOUNT": "TOKENIZE",

    # =====================================
    # NAME
    # =====================================

    "NAME": "PARTIAL_MASK",

    # =====================================
    # ORG
    # =====================================

    "ORG": "MASK"
}


# =========================================
# APPLY POLICY RULES
# =========================================

def apply_policy_rules(entities):

    updated_entities = []

    for entity in entities:

        label = entity["label"]

        # =================================
        # CONTEXT VERIFIED ORG
        # =================================

        if (

            label == "ORG"

            and

            entity.get(
                "validated",
                False
            )

        ):

            action = "MASK"

        # =================================
        # STANDARD POLICY
        # =================================

        else:

            action = POLICY_RULES.get(

                label,

                "MASK"
            )

        entity["policy_action"] = action

        updated_entities.append(
            entity
        )

    return updated_entities